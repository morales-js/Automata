    private void btnnuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnuevoActionPerformed
    try {
        // Crear archivo temporal con el texto que el usuario escribió
        File temp = File.createTempFile("automata_manual", ".txt");
        BufferedWriter bw = new BufferedWriter(new FileWriter(temp));
        bw.write(txtarea.getText());
        bw.close();

        // Leer el automata usando tu clase ya existente
        Modelo.Automata automata = Modelo.LectorDeArchivo.leerDesdeArchivo(temp);

        // Guardar el automata como atributo de esta ventana
        this.automataCreado = automata;

        JOptionPane.showMessageDialog(this, "Autómata creado correctamente.");
        this.dispose();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error al procesar el texto: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnnuevoActionPerformed

